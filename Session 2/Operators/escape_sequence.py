'''
\n - New line
\t - Tab space
\\ - Prints a backslash
\" - Prints a double quote
\' - Prints a single quote
'''

print("Hello\nWorld")
print("Hello\tWorld")
print("Hello\\World")  # Hello\World

# My Name is "Vaidik" Gampawar
print("My Name is \"Vaidik\" Gampawar")
print('My Name is \'Vaidik\' Gampawar')
